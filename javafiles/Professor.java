package jdbc;

public class Professor {
	int profid;
	String login;
	String pass;
	int prof_dept;
	public int getProfid() {
		return profid;
	}
	public void setProfid(int profid) {
		this.profid = profid;
	}
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public int getProf_dept() {
		return prof_dept;
	}
	public void setProf_dept(int prof_dept) {
		this.prof_dept = prof_dept;
	}
	
	
	

}
